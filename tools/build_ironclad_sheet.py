"""Build the loader-shaped real_ironclad artifacts from local inputs.

This is the last-mile step the loader docstring names
(extract_base_game_pool.py -> build_ironclad_sheet.py). The loader needs TWO
files to instantiate real_ironclad, and neither drops out of the extractor
ready to use:

  game_ref/ironclad_pool.yaml       -- a SINGLE-document 57-card list
  game_ref/char_real_ironclad.yaml  -- the character entry (hp, deck, relics)

Inputs (all LOCAL / gitignored in game_ref/, never committed -- base-game
reference material):

  ironclad-cards.yaml        extractor output; TWO docs (doc 1 = the 35 cards
                             the JSON extractor can express, doc 2 = excluded:).
  ironclad_pool_pass4.yaml   the 22 "pass-4" cards, hand-recovered from the
                             sts2.dll PowerVar/DynamicVar ctor args the JSON
                             extractor cannot parse.
  ironclad_char_facts.yaml   the character facts (id/name/hp/starting_deck/
                             relic_hooks), decompiled from the Ironclad model.

Only THIS TOOL is committed; every data input stays in game_ref/.

Guarantees (the review findings this closes):
  * FAIL CLOSED. If the supplement or the char facts are missing, the build
    ERRORS before writing anything -- it never overwrites a full reference
    with a 35-card stub.
  * NO SILENT DRIFT. doc 1 and the supplement must be disjoint; any shared id
    is an error (if the extractor starts expressing a card, its stale hand-
    recovered twin must be pruned from the supplement, not silently win).
    Duplicate ids within either input are errors too.
  * HONEST VERIFY. --verify checks the on-disk pool for duplicate ids and
    compares against the rebuild as an ORDERED list (not just id-keyed dicts),
    so ordering and duplication differences surface rather than hide.

Usage (from repo root):
  python -m tools.build_ironclad_sheet --split    # bootstrap supplement+facts
  python -m tools.build_ironclad_sheet            # rebuild pool (+ char if missing)
  python -m tools.build_ironclad_sheet --verify   # validate current artifacts
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path

import yaml

REPO = Path(__file__).resolve().parent.parent
GAME_REF = REPO / "game_ref"
EXTRACTOR_SHEET = GAME_REF / "ironclad-cards.yaml"    # 2-doc; we read doc 1
SUPPLEMENT = GAME_REF / "ironclad_pool_pass4.yaml"    # local, gitignored
CHAR_FACTS = GAME_REF / "ironclad_char_facts.yaml"    # local, gitignored
POOL = GAME_REF / "ironclad_pool.yaml"                # loader input
CHAR_OUT = GAME_REF / "char_real_ironclad.yaml"       # loader input

CHAR_FIELDS = ("id", "name", "hp", "starting_deck", "relic_hooks")

POOL_HEADER = """\
# MACHINE-GENERATED by tools/build_ironclad_sheet.py -- do not hand-edit.
# loader.EXTERNAL_CARD_SHEETS reads this as a single-document card list.
# Two LOCAL inputs (game_ref/, gitignored): ironclad-cards.yaml document 1
# (extractor) + ironclad_pool_pass4.yaml (pass-4 supplement, DLL-sourced).
# Rebuild:  python -m tools.build_ironclad_sheet
"""

SUPPLEMENT_HEADER = """\
# LOCAL ARTIFACT -- game_ref/ is gitignored. The "pass-4" supplement: cards
# recovered by hand from the sts2.dll ctor args the JSON extractor cannot
# parse (hence absent from ironclad-cards.yaml document 1). Must stay DISJOINT
# from document 1. Merged into ironclad_pool.yaml by build_ironclad_sheet.py.
"""

CHAR_FACTS_HEADER = """\
# LOCAL ARTIFACT -- game_ref/ is gitignored. Decompiled Ironclad character
# facts (ilspycmd -t MegaCrit.Sts2.Core.Models.Characters.Ironclad): StartingHp,
# StartingDeck, StartingRelics=>BurningBlood (=> heal_after_won_fight). The
# loader-required fields only; build_ironclad_sheet.py emits char_real_ironclad.
"""

CHAR_OUT_HEADER = """\
# MACHINE-GENERATED by tools/build_ironclad_sheet.py from local
# game_ref/ironclad_char_facts.yaml -- loader-shaped stub. The design rulings
# for real_ironclad (why packages/constraints/bands are deliberately absent)
# live in the hand-authored artifact / design notes, not in this generated
# file. LOCAL: never copy into docs/ or tier0/content/.
"""


def _fail(msg: str) -> "None":
    sys.exit(f"build_ironclad_sheet: {msg}")


def _rel(p: Path) -> str:
    return str(p.relative_to(REPO))


def _dup_ids(cards: list[dict]) -> list[str]:
    seen: set[str] = set()
    dupes: list[str] = []
    for c in cards:
        cid = c["id"]
        if cid in seen and cid not in dupes:
            dupes.append(cid)
        seen.add(cid)
    return sorted(dupes)


def _doc1_cards() -> list[dict]:
    if not EXTRACTOR_SHEET.exists():
        _fail(f"missing {_rel(EXTRACTOR_SHEET)} -- run extract_base_game_pool.py "
              "first (needs the local sts2.dll).")
    docs = list(yaml.safe_load_all(EXTRACTOR_SHEET.read_text()))
    if not docs or not isinstance(docs[0], list):
        _fail(f"{EXTRACTOR_SHEET.name}: document 1 is not a card list.")
    return docs[0]


def _dump(obj, header: str, path: Path) -> None:
    body = yaml.safe_dump(obj, sort_keys=False, default_flow_style=False,
                          allow_unicode=True, width=100)
    path.write_text(header + body)


# --- validation shared by build and verify ------------------------------

def _validated_pool_cards() -> list[dict]:
    """doc1 + supplement, fully validated, sorted by id. Fails closed."""
    doc1 = _doc1_cards()
    if not SUPPLEMENT.exists():
        _fail(f"missing {_rel(SUPPLEMENT)} -- refusing to write a partial pool "
              f"({len(doc1)} extractor cards only, not the measured 57). "
              "Recover it with --split from an existing pool, or restore it.")
    supp = yaml.safe_load(SUPPLEMENT.read_text()) or []
    if not supp:
        _fail(f"{SUPPLEMENT.name} is empty -- refusing to write a partial pool.")

    d_dup, s_dup = _dup_ids(doc1), _dup_ids(supp)
    if d_dup:
        _fail(f"duplicate ids in extractor doc 1: {d_dup}")
    if s_dup:
        _fail(f"duplicate ids in supplement: {s_dup}")
    overlap = sorted({c["id"] for c in doc1} & {c["id"] for c in supp})
    if overlap:
        _fail(f"supplement overlaps extractor doc 1 on {overlap} -- the "
              "extractor now expresses these, so the stale hand-recovered "
              "copies must be REMOVED from the supplement (extractor wins).")
    merged = doc1 + supp
    return sorted(merged, key=lambda c: c["id"])


def _char_facts() -> dict:
    if not CHAR_FACTS.exists():
        _fail(f"missing {_rel(CHAR_FACTS)} -- cannot build the character entry. "
              "Recover it with --split from an existing char_real_ironclad.yaml.")
    facts = yaml.safe_load(CHAR_FACTS.read_text()) or {}
    missing = [k for k in CHAR_FIELDS if k not in facts]
    if missing:
        _fail(f"{CHAR_FACTS.name} missing required fields {missing}.")
    if not facts["starting_deck"]:
        _fail(f"{CHAR_FACTS.name}: starting_deck is empty.")
    return facts


# --- commands -----------------------------------------------------------

def do_split() -> None:
    doc1_ids = {c["id"] for c in _doc1_cards()}
    if POOL.exists():
        pool = yaml.safe_load(POOL.read_text()) or []
        supp = sorted((c for c in pool if c["id"] not in doc1_ids),
                      key=lambda c: c["id"])
        _dump(supp, SUPPLEMENT_HEADER, SUPPLEMENT)
        print(f"wrote {_rel(SUPPLEMENT)}: {len(supp)} supplement cards "
              f"({len(pool)} pool - {len(doc1_ids)} extractor).")
    else:
        print(f"no {POOL.name}; skipped supplement bootstrap.", file=sys.stderr)
    if CHAR_OUT.exists():
        cur = yaml.safe_load(CHAR_OUT.read_text()) or {}
        facts = {k: cur[k] for k in CHAR_FIELDS if k in cur}
        miss = [k for k in CHAR_FIELDS if k not in facts]
        if miss:
            _fail(f"{CHAR_OUT.name} missing {miss}; cannot bootstrap facts.")
        _dump(facts, CHAR_FACTS_HEADER, CHAR_FACTS)
        print(f"wrote {_rel(CHAR_FACTS)}: character facts "
              f"({facts['hp']} hp, {len(facts['starting_deck'])}-card deck).")
    else:
        print(f"no {CHAR_OUT.name}; skipped char-facts bootstrap.",
              file=sys.stderr)


def do_build() -> None:
    cards = _validated_pool_cards()          # fails closed before any write
    facts = _char_facts()                    # fails closed before any write
    _dump(cards, POOL_HEADER, POOL)
    print(f"wrote {_rel(POOL)}: {len(cards)} cards.")
    if CHAR_OUT.exists():
        print(f"kept {_rel(CHAR_OUT)} (hand-authored; not overwritten).")
    else:
        entry = {k: facts[k] for k in CHAR_FIELDS}
        _dump(entry, CHAR_OUT_HEADER, CHAR_OUT)
        print(f"wrote {_rel(CHAR_OUT)}: bootstrapped from char facts.")


def do_verify() -> None:
    if not POOL.exists():
        _fail(f"--verify needs an existing {_rel(POOL)}.")
    current = yaml.safe_load(POOL.read_text()) or []
    cur_dup = _dup_ids(current)
    if cur_dup:
        _fail(f"{POOL.name} has duplicate ids: {cur_dup}")
    rebuilt = _validated_pool_cards()        # validates inputs too
    ok = True
    if [c["id"] for c in current] != [c["id"] for c in rebuilt]:
        ok = False
        cur_ids, new_ids = {c["id"] for c in current}, {c["id"] for c in rebuilt}
        if cur_ids - new_ids:
            print(f"  in pool, not in rebuild: {sorted(cur_ids - new_ids)}")
        if new_ids - cur_ids:
            print(f"  in rebuild, not in pool: {sorted(new_ids - cur_ids)}")
        if cur_ids == new_ids:
            print("  same id set but DIFFERENT ORDER (pool is not sorted by id)")
    else:
        diff = [a["id"] for a, b in zip(current, rebuilt) if a != b]
        if diff:
            ok = False
            print(f"  cards differ in fields: {diff}")
    if not CHAR_OUT.exists():
        ok = False
        print(f"  missing {_rel(CHAR_OUT)} -- real_ironclad cannot instantiate.")
    else:
        cur = yaml.safe_load(CHAR_OUT.read_text()) or {}
        miss = [k for k in CHAR_FIELDS if k not in cur]
        if miss:
            ok = False
            print(f"  {CHAR_OUT.name} missing loader fields {miss}.")
    if ok:
        print(f"VERIFY OK: {len(current)} pool cards match the rebuild "
              "(ordered, dup-checked); char entry loader-complete.")
    else:
        _fail("VERIFY FAILED (see above).")


if __name__ == "__main__":
    ap = argparse.ArgumentParser(description=__doc__.split("\n")[0])
    g = ap.add_mutually_exclusive_group()
    g.add_argument("--split", action="store_true",
                   help="bootstrap the local supplement + char facts from "
                        "existing artifacts")
    g.add_argument("--verify", action="store_true",
                   help="validate the current pool + char entry against a rebuild")
    args = ap.parse_args()
    if args.split:
        do_split()
    elif args.verify:
        do_verify()
    else:
        do_build()
